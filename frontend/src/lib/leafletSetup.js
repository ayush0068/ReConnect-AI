// Vite (like Webpack) breaks Leaflet's default marker icon resolution
// because it rewrites asset URLs at build time. Re-pointing the default
// icon at the bundled asset URLs fixes the classic "broken image" pin
// that shows up otherwise. Import this once, before any map renders
// (see main.jsx).
import L from 'leaflet';
import markerIcon2x from 'leaflet/dist/images/marker-icon-2x.png';
import markerIcon from 'leaflet/dist/images/marker-icon.png';
import markerShadow from 'leaflet/dist/images/marker-shadow.png';

delete L.Icon.Default.prototype._getIconUrl;
L.Icon.Default.mergeOptions({
  iconRetinaUrl: markerIcon2x,
  iconUrl: markerIcon,
  shadowUrl: markerShadow,
  iconSize: [25, 41],
  iconAnchor: [12, 41],
  popupAnchor: [1, -34],
  shadowSize: [41, 41],
});